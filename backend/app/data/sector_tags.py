def get_sector(ticker: str, fallback: str = "Unknown") -> str:
    """Look up the GICS sector for a ticker symbol.

    Checks the static ``SECTOR_MAP`` first. Returns ``fallback`` if the
    ticker is not found.

    Args:
        ticker: Ticker symbol (case-insensitive; normalised to uppercase).
        fallback: Value to return when the ticker is not in the static map.
            Defaults to ``"Unknown"``.

    Returns:
        GICS sector name string (e.g. ``"Information Technology"``).

    Example::

        >>> get_sector("AAPL")
        'Information Technology'
        >>> get_sector("UNKNOWN")
        'Unknown'
    """
    return SECTOR_MAP.get(ticker.strip().upper(), fallback)


def enrich_sector_map(
    tickers: list[str],
    yfinance_map: dict[str, str] | None = None,
) -> dict[str, str]:
    """Build a complete sector map for the given tickers.

    Resolution order (highest priority first):
    1. ``yfinance_map`` — live metadata returned by the data fetcher.
    2. Static ``SECTOR_MAP`` — curated offline mapping.
    3. ``"Unknown"`` — fallback when neither source has data.

    The yfinance map takes priority because it reflects the most current
    classification, while the static map provides a reliable offline
    fallback for well-known tickers.

    Args:
        tickers: List of ticker symbols to resolve sectors for.
        yfinance_map: Optional dict of ``{ticker: sector}`` from yfinance.
            Values of ``None``, ``""`` or ``"Unknown"`` are treated as
            missing and the static map is consulted next.

    Returns:
        Dict mapping each ticker to its resolved sector name.

    Example::

        >>> enrich_sector_map(["AAPL", "MSFT"], {"MSFT": "Technology"})
        {'AAPL': 'Information Technology', 'MSFT': 'Technology'}
    """
    yf_map = yfinance_map or {}
    result: dict[str, str] = {}

    for ticker in tickers:
        upper = ticker.strip().upper()

        # 1. yfinance live data (skip if missing/unknown)
        yf_sector = yf_map.get(upper) or yf_map.get(ticker)
        if yf_sector and yf_sector.strip() and yf_sector.strip() != "Unknown":
            result[upper] = yf_sector.strip()
            continue

        # 2. Static map
        static_sector = SECTOR_MAP.get(upper)
        if static_sector:
            result[upper] = static_sector
            continue

        # 3. Fallback
        result[upper] = "Unknown"

    return result


def get_tickers_by_sector(sector: str) -> list[str]:
    """Return all tickers in the static map that belong to ``sector``.

    The comparison is case-insensitive and strips leading/trailing whitespace.

    Args:
        sector: GICS sector name (e.g. ``"Information Technology"``).

    Returns:
        Sorted list of ticker symbols in that sector. Empty list if the
        sector is not found in the static map.

    Example::

        >>> tickers = get_tickers_by_sector("Energy")
        >>> "XOM" in tickers
        True
    """
    normalised = sector.strip().lower()
    return sorted(
        ticker
        for ticker, sec in SECTOR_MAP.items()
        if sec.strip().lower() == normalised
    )


def is_valid_gics_sector(sector: str) -> bool:
    """Check whether ``sector`` is a recognised GICS top-level sector.

    Args:
        sector: Sector name to validate.

    Returns:
        True if ``sector`` is one of the 11 GICS top-level sectors.

    Example::

        >>> is_valid_gics_sector("Information Technology")
        True
        >>> is_valid_gics_sector("Tech")
        False
    """
    return sector.strip() in GICS_SECTORS


