"""Sector tagging utilities for portfolio assets.

Provides a curated static mapping of well-known ticker symbols to their
GICS (Global Industry Classification Standard) sectors, plus helper
functions to look up, enrich, and validate sector assignments.

Why a static map?
-----------------
yfinance's ``Ticker.info`` call is slow (one HTTP request per ticker) and
occasionally returns stale or missing sector data. The static map provides
instant, reliable sector tags for the most commonly traded US equities.
For tickers not in the static map the module falls back to yfinance metadata
(already fetched by the data fetcher) and finally to ``"Unknown"``.

GICS Sectors (11 top-level)
----------------------------
1. Communication Services
2. Consumer Discretionary
3. Consumer Staples
4. Energy
5. Financials
6. Health Care
7. Industrials
8. Information Technology
9. Materials
10. Real Estate
11. Utilities

Usage::

    from app.data.sector_tags import (
        get_sector,
        enrich_sector_map,
        get_tickers_by_sector,
        SECTOR_MAP,
        GICS_SECTORS,
    )

    # Single lookup
    sector = get_sector("AAPL")          # "Information Technology"
    sector = get_sector("UNKNOWN_TICK")  # "Unknown"

    # Enrich a partial sector_map returned by yfinance
    enriched = enrich_sector_map(
        tickers=["AAPL", "MSFT", "XYZ"],
        yfinance_map={"XYZ": "Energy"},
    )
    # {"AAPL": "Information Technology", "MSFT": "Information Technology", "XYZ": "Energy"}

    # Get all tickers in a sector
    tech_tickers = get_tickers_by_sector("Information Technology")
"""

