/**
 * Badge component — shadcn/ui implementation.
 *
 * Provides small status indicators for optimization run status,
 * solver status, and other categorical labels.
 *
 * Variants: default | secondary | destructive | outline | quantum | classical
 *
 * Usage:
 *   <Badge>Completed</Badge>
 *   <Badge variant="destructive">Failed</Badge>
 *   <Badge variant="quantum">QAOA</Badge>
 *   <Badge variant="classical">Markowitz</Badge>
 */

import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";
import { cn } from "@/lib/utils";

const badgeVariants = cva(
  "inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default:
          "border-transparent bg-primary text-primary-foreground hover:bg-primary/80",
        secondary:
          "border-transparent bg-secondary text-secondary-foreground hover:bg-secondary/80",
        destructive:
          "border-transparent bg-destructive text-destructive-foreground hover:bg-destructive/80",
        outline:
          "text-foreground",
        success:
          "border-transparent bg-green-100 text-green-800 dark:bg-green-900/30 dark:text-green-400",
        warning:
          "border-transparent bg-yellow-100 text-yellow-800 dark:bg-yellow-900/30 dark:text-yellow-400",
        quantum:
          "border-transparent bg-quantum text-quantum-foreground hover:bg-quantum/80",
        classical:
          "border-transparent bg-classical text-classical-foreground hover:bg-classical/80",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  },
);

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return (
    <div className={cn(badgeVariants({ variant }), className)} {...props} />
  );
}

// eslint-disable-next-line react-refresh/only-export-components
export { Badge, badgeVariants };
